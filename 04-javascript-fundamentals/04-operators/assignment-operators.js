// Assignment Operators
// ( = , += , -= , *= , /= , %= , **= )

// Assignment
let numberOne = 10;
let numberTwo = numberOne; // numberOne = 10, numberTwo = 10
console.log("first number: ",numberOne, " second number: ", numberTwo);


// Addition Assignment
numberOne = 10, numberTwo = 5;
numberOne += numberTwo;             // numberOne = numberOne + numberTwo
console.log("first number: ",numberOne, " second number: ", numberTwo);


// Subtraction Assignment
numberOne = 10, numberTwo = 5;
numberOne -= numberTwo;             // numberOne = numberOne - numberTwo
console.log("first number: ",numberOne, " second number: ", numberTwo);


// Multiplication Assignment
numberOne = 10, numberTwo = 5;
numberOne *= numberTwo;             // numberOne = numberOne * numberTwo
console.log("first number: ",numberOne, " second number: ", numberTwo);

// Division Assignment
numberOne = 10, numberTwo = 5;
numberOne /= numberTwo;             // numberOne = numberOne / numberTwo
console.log("first number: ",numberOne, " second number: ", numberTwo);

// Remainder Assignment
numberOne = 10, numberTwo = 5;
numberOne %= numberTwo;             // numberOne = numberOne % numberTwo
console.log("first number: ",numberOne, " second number: ", numberTwo);

// Exponentiation Assignment
numberOne = 10, numberTwo = 5;
numberOne **= numberTwo;            // numberOne = numberOne ** numberTwo
console.log("first number: ",numberOne, " second number: ", numberTwo);

