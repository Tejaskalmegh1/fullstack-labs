let numberOne = 10;
let numberTwo = 5;

let ch = 1;

switch(ch) {

        case 1:
        console.log("Addition:",numberOne+numberTwo);
        break;
        
        case 2:
        console.log("Sub:",numberOne+numberTwo);
        break;
        
        case 3:
        console.log("Multi:",numberOne*numberTwo);
        break;
        
        case 4:
        console.log("Div:",numberOne/numberTwo);
        break;
        
        default:
        console.log("Invalid choice...");
        break;
        
}
